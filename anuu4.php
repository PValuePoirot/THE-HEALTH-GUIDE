<p>
     Different Types of Ketogenic Diets
There are several versions of the ketogenic diet, including:
<p>

Standard ketogenic diet (SKD): This is a very low-carb, moderate-protein and high-fat diet. It typically contains 75% fat, 20% protein and only 5% carbs (1Trusted Source).
</p>
<p>Cyclical ketogenic diet (CKD): This diet involves periods of higher-carb refeeds, such as 5 ketogenic days followed by 2 high-carb days.
</p><p>Targeted ketogenic diet (TKD): This diet allows you to add carbs around workouts.
</p><p>High-protein ketogenic diet: This is similar to a standard ketogenic diet, but includes more protein. The ratio is often 60% fat, 35% protein and 5% carbs.
</p><p>However, only the standard and high-protein ketogenic diets have been studied extensively. Cyclical or targeted ketogenic diets are more advanced methods and primarily used by bodybuilders or athletes.
</p>
The information in this article mostly applies to the standard ketogenic diet (SKD), although many of the same principles also apply to the other versions.
