<p>
     How to Build Muscle
The biggest muscle building mistake people make is training like a bodybuilder.</p><p>
 Many bodybuilders use drugs but won’t tell you. And they rarely built the bulk of their muscle size with the routines they do now. That’s why bodybuilding routines don’t work for most people. 
</p>
<p>This is what works…

</p>
<p>
Get Stronger. The best bodybuilders that ever existed were strong. They knew more strength is more muscle. Increase your Squat to 140kg/300lb, Bench Press to 100kg/220lb, and Deadlift to 180kg/400lb. Your overall muscle mass will increase because strength is size.
</p>
<p>
Add Weight. 
</p>
<p>
Forget about pump and soreness. Instead focus on adding weight on the bar. Try to lift more than last time. You’ll get stronger which will increase your overall muscle mass. If you don’t lift more today than last month or year, you’re not building muscle.
</p>
<p>
Do Compounds.
</p>
<p> Do exercises that work several muscles at the same time. You’ll be able to lift heavier weights which will trigger more muscle growth. The bulk of your routine should consist of heavy Squats, Bench, Deadlifts, Overhead Presses and Barbell Rows.
</p>
<p>Use Barbells. 
</p>
<p>
</p>You can lift heavier weights with barbells than any other tools. You also have to balance the weight yourself. Barbells involve more muscles and trigger more growth. Use free weights not attached to machines. Start light and use proper form to avoid injury.
<p>
Increase Frequency. </p>
<p>
The more often you train a muscle, the more you trigger it to grow. The more you do an exercise, the quicker your technique improves, and the heavier you can lift. Start Squatting, pressing and pulling three times a week instead of only once.
</p>
<p>Recover. Your muscles need to recover from your workouts to grow stronger and bigger. They can’t recover if you work them hard every day. Even your mind needs a break. Take three to four rest days a week. Help your muscles recover by getting plenty of food, water and sleep.
</p>Eat More. Your body uses food to fuel workouts, and recover muscles. Your muscles can’t recover and grow if there’s a shortage of food. Most guys need at least 3000kcal/day to build muscle. Skinny guys with high metabolisms need even more to gain weight.
<p>Eat Protein. Your body uses protein to build new muscle, and recover damaged muscle tissue post-workout. You need about 1.8g protein per kilogram of body-weight (0.82g/lb) to maximize muscle recovery and build muscle. For an 80kg/175lb guy, that’s about 135g of protein per day.
</p>Get Real. You can’t get steroid-like results without using steroids. You can’t turn into Arnold in three months because building muscle takes time. Celebrities set unrealistic expectations. Stop trying to look like them. Focus on improving yourself. It will save you frustration.
<p>Be Consistent. Most guys gain 0.25kg/0.5lb of lean muscle per week if they do an effective training program and eat well. You can’t gain muscle faster than this. It takes a year to gain 12kg/24lb of lean muscle and see dramatic change. Consistency is therefore key.
</p>

